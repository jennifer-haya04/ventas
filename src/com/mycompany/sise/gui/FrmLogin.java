    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        String nombre, clave;
        nombre = txtUsuario.getText();
        
        char[] array = password.getPassword();
        clave = new String(array);
        EmpleadoLogic logic = new EmpleadoLogic();
        try {
            Empleado objEmpleado = logic.validarAcceso(nombre, clave);
            
            if(objEmpleado == null){
                JOptionPane.showMessageDialog(null, "Usuario o clave incorrecto");
            }else{
                JOptionPane.showMessageDialog(null, "Bienvenido: " + objEmpleado.getNombre());
                FrmPrincipal frm = new FrmPrincipal();
                frm.setExtendedState(MAXIMIZED_BOTH);
                frm.setVisible(true);
                this.setVisible(false);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocurri√≥ un problema al Validar el Acceso", "SISTEMA", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

