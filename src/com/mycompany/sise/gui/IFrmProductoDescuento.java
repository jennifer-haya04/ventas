    ProductoDescuento getProdDesc(){
        
        int anioI, mesI, diaI, anioF, mesF, diaF;
        anioI = dtpFechaInicio.getModel().getYear();
        mesI = dtpFechaInicio.getModel().getMonth()+1;
        diaI = dtpFechaInicio.getModel().getDay();
        anioF = dtpFechaFin.getModel().getYear();
        mesF = dtpFechaFin.getModel().getMonth()+1;
        diaF = dtpFechaFin.getModel().getDay();
        
        return new ProductoDescuento(
            ((Producto)cboProducto.getSelectedItem()).getId_producto(),
            ((Descuento)cboDescuento.getSelectedItem()).getId_descuento(),
            LocalDate.of(anioI, mesI, diaI),
            LocalDate.of(anioF, mesF, diaF)
            );
        
    }
    
