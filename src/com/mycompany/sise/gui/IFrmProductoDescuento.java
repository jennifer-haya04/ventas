    private void filtroComboProducto(){
  
        llenarComboProducto();
    
        cboProducto.getEditor().getEditorComponent().addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent evt){
                String filtro = cboProducto.getEditor().getItem().toString();
                
                if(evt.getKeyCode()>=65 && evt.getKeyCode()<=90 || evt.getKeyCode() >= 96 && evt.getKeyCode() <= 105 || evt.getKeyCode() ==8){
                    try {
                        logicProducto = new ProductoLogic();
                        cboProducto.setModel(logicProducto.getListaProducto(filtro));
                        if(cboProducto.getItemCount()>0){
                            cboProducto.showPopup();
                            if(evt.getKeyCode()!=8){
                                ((JTextComponent)cboProducto.getEditor().getEditorComponent()).select(filtro.length(),cboProducto.getEditor().getItem().toString().length());
                            }else{
                                cboProducto.getEditor().setItem(filtro);
                            }
                        }
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, ex, "Sistema", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        });
    }
    
    
