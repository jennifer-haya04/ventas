    private void filtroComboSucursal(){
  
        llenarComboSucursal();
    
        cboSucursal.getEditor().getEditorComponent().addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent evt){
                String filtro = cboSucursal.getEditor().getItem().toString();
                
                if(evt.getKeyCode()>=65 && evt.getKeyCode()<=90 || evt.getKeyCode() >= 96 && evt.getKeyCode() <= 105 || evt.getKeyCode() ==8){
                    try {
                        logicSucursal = new SucursalLogic();
                        cboSucursal.setModel(logicSucursal.getListaSucursal(filtro));
                        if(cboSucursal.getItemCount()>0){
                            cboSucursal.showPopup();
                            if(evt.getKeyCode()!=8){
                                ((JTextComponent)cboSucursal.getEditor().getEditorComponent()).select(filtro.length(),cboSucursal.getEditor().getItem().toString().length());
                            }else{
                                cboSucursal.getEditor().setItem(filtro);
                            }
                        }
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, ex, "Sistema", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        });
    }
    
