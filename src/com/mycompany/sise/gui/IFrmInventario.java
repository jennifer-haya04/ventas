    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        logic = new InventarioLogic();
        
        if(txtStock.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No olvides ingresar el Stock");
        }else{
            Inventario objInventario = getInventario();

            try {
                Helper oHelperLogic = logic.insertarValidado(objInventario);
                if(oHelperLogic.getError()){
                    JOptionPane.showMessageDialog(null, oHelperLogic.getMensajeUsuario() );
                } else {
                    if(objInventario.getId_producto()>0 || objInventario.getId_sucursal()>0){
                        JOptionPane.showMessageDialog(null, rb.getString("mensaje_agregar"));
                    }

                    logic.imprimirTable(tbLista);
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex, "Sistema", JOptionPane.ERROR_MESSAGE);
            }

            limpiar();
        }
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

