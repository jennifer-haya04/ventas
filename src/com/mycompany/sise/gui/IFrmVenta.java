    private void tbListaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListaMouseReleased
        
        try {
            logicPersona = new PersonaLogic();
            logicDocVenta = new TipoDocVentaLogic();
            logicPago = new TipoPagoLogic();
            logicSucursal = new SucursalLogic();
            int fila ;
            fila = tbLista.getSelectedRow();
            int id, id_persona=-1, id_doc = -1, id_pago = -1, id_sucursal = -1;
            double total, totalDesc;
            LocalDate fecha;
        
            logic.setModeloProducto(null);
            
            if(fila>=0){
                int i=0;
                id = Integer.parseInt(tbLista.getValueAt(fila, i++)+"");
                id_persona = Integer.parseInt(tbLista.getValueAt(fila, i++)+"");
                id_doc = Integer.parseInt(tbLista.getValueAt(fila, i++)+"");
                id_pago = Integer.parseInt(tbLista.getValueAt(fila, i++)+"");
                id_sucursal = Integer.parseInt(tbLista.getValueAt(fila, i++)+"");
                fecha = LocalDate.parse(tbLista.getValueAt(fila, i++)+"");
                totalDesc = Double.parseDouble(tbLista.getValueAt(fila, i++)+"");
                total = Double.parseDouble(tbLista.getValueAt(fila, i++)+"");

                btnAgregar.setEnabled(false);
                txtId.setText(id+"");
                logicPersona.selectItemByIDCombo(cboCliente, id_persona);
                logicDocVenta.selectItemByIDCombo(cboDoc, id_doc);
                logicPago.selectItemByIDCombo(cboPago, id_pago);
                logicSucursal.selectItemByIDCombo(cboSucursal, id_sucursal);
                dtpfecha.getModel().setDate(
                    fecha.getYear(), //aÃ±o
                    fecha.getMonthValue()-1, //mes
                    fecha.getDayOfMonth()); // dia
                dtpfecha.getModel().setSelected(true);
                txtDesc.setText(totalDesc+"");
                txtTotal.setText(total+"");
                
                logic.imprimirProductos(tbProductos, id);
            }
                
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Sistema", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_tbListaMouseReleased

