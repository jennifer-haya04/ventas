    LocalDate getFecha(){
       int anio, mes, dia;
        anio = dtpfecha.getModel().getYear();
        mes = dtpfecha.getModel().getMonth()+1;
        dia = dtpfecha.getModel().getDay();
        return LocalDate.of(anio, mes, dia);
    }
    
    Venta getVenta(){
        
        
        return new Venta(
                Integer.parseInt(txtId.getText()),
                ((Persona)cboCliente.getSelectedItem()).getId_persona(),
                ((TipoDocVenta)cboDoc.getSelectedItem()).getId_tipo_doc(),
                ((TipoPago)cboPago.getSelectedItem()).getId_tipo_pago(),
                ((Sucursal)cboSucursal.getSelectedItem()).getId_sucursal(),
                getFecha(),
                Double.parseDouble(txtDesc.getText()),
                Double.parseDouble(txtTotal.getText())
        );
    }
    
    
    
