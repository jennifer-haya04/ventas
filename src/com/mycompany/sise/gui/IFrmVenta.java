    private void filtroComboPersona(){
  
        llenarComboPersona();
    
        cboCliente.getEditor().getEditorComponent().addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent evt){
                String filtro = cboCliente.getEditor().getItem().toString();
                
                if(evt.getKeyCode()>=65 && evt.getKeyCode()<=90 || evt.getKeyCode() >= 96 && evt.getKeyCode() <= 105 || evt.getKeyCode() ==8){
                    try {
                        logicPersona = new PersonaLogic();
                        cboCliente.setModel(logicPersona.getListaPersona(filtro));
                        if(cboCliente.getItemCount()>0){
                            cboCliente.showPopup();
                            if(evt.getKeyCode()!=8){
                                ((JTextComponent)cboCliente.getEditor().getEditorComponent()).select(filtro.length(),cboCliente.getEditor().getItem().toString().length());
                            }else{
                                cboCliente.getEditor().setItem(filtro);
                            }
                        }
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, ex, "Sistema", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        });
    }
    
