    private void tbProductosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbProductosKeyReleased
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            int fila, id_prod ;
            fila = tbProductos.getSelectedRow();
            int cantidad, cantidadInventario;
            double total, precio, descuento, totalDesc;
            
            try {
                if(fila>=0){
                    id_prod = Integer.parseInt(tbProductos.getValueAt(fila, 0)+"");
                    cantidad = Integer.parseInt(tbProductos.getValueAt(fila, 1)+"");
                    
                    cantidadInventario = logic.obtenerCantidadInventario(id_prod, ((Sucursal)cboSucursal.getSelectedItem()).getId_sucursal());
                    
                    if(cantidad > cantidadInventario){
                        JOptionPane.showMessageDialog(null, "Stock Disponible: " + cantidadInventario, "CANTIDAD INSUFICIENTE", JOptionPane.WARNING_MESSAGE);
                    }else{
                        precio = Double.parseDouble(tbProductos.getValueAt(fila, 4)+"");
                        total = cantidad * precio;
                        descuento = getDescProducto();
                        totalDesc = descuento *  total;

                        tbProductos.setValueAt(total, fila, 2);
                        tbProductos.setValueAt(totalDesc, fila, 5);

                        logic.llenarTotales(txtDesc, txtTotal, logic.getModeloProducto());
                    }
                   
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            } 
        }
    }//GEN-LAST:event_tbProductosKeyReleased

