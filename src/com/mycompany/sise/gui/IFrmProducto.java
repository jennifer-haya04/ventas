    String guardarImagen() {
        String nameImage = null;
        try {
            UUID uuidNameImage = UUID.randomUUID();
            nameImage = uuidNameImage.toString() + ".png";
            
            Path origenPath = Paths.get(rutaImagenProd);
            Path destinoPath = Paths.get(rbApp.getString("ruta_imagenes") + File.separator + nameImage);
            Files.copy(origenPath, destinoPath, StandardCopyOption.REPLACE_EXISTING);
        } catch(Exception e){
            System.out.println("Error al guardar imagem");
        }
        
        return nameImage;
    }
    
    Producto getProducto(){
        Producto oProducto = null;
        String nameImage = guardarImagen();
        
        if(nameImage != null) {
            oProducto =  new Producto(
                    Integer.parseInt(txtId.getText()),
                    ((CategoriaProducto)cboCategoria.getSelectedItem()).getId_Cat(),
                    txtModelo.getText(),
                    txtTalla.getText(),
                    txtColor.getText(),
                    Double.parseDouble(txtPrecio.getText()),
                    nameImage
            );
        }
        
        return oProducto;
    }
    
