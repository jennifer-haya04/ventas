    private void tbListaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListaMouseReleased
        
        try {
            logicCategoria = new CategoriaProductoLogic();
            int fila ;
            fila = tbLista.getSelectedRow();
            int id, id_categoria=-1;
            String nombre, modelo, talla, color, nombreImage;
            Double precio;
        
            if(fila>=0){
                int i=0;
                id = Integer.parseInt(tbLista.getValueAt(fila, i++)+"");
                id_categoria = Integer.parseInt(tbLista.getValueAt(fila, i++)+"");
                modelo = tbLista.getValueAt(fila, i++)+"";
                talla = tbLista.getValueAt(fila, i++)+"";
                color = tbLista.getValueAt(fila, i++)+"";
                precio = Double.parseDouble(tbLista.getValueAt(fila, i++)+"");
                nombreImage = tbLista.getValueAt(fila, i++)+"";

                btnAgregar.setEnabled(false);
                txtId.setText(id+"");
                logicCategoria.selectItemByIDCombo(cboCategoria, id_categoria);
                txtModelo.setText(modelo);
                txtTalla.setText(talla);
                txtColor.setText(color);
                txtPrecio.setText(precio+"");
                
                nombreImage = rbApp.getString("ruta_imagenes") + File.separator + nombreImage;
                
                ImageIcon imagen = new ImageIcon(nombreImage);
                lblimagen.setIcon(imagen);
            }
                
        } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e, "Sistema", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_tbListaMouseReleased

